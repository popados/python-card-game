def add_nums(x,y) :
    print('first number :', x)
    print('second number :', y)
    z = int(x) + int(y)
    print('\nadded: ',z)


def subtract_nums(x,y) :
    # print('first # :', x)
    # print('second number :', y)
    z = int(x) - int(y)
    print('\nsubtracted: ',z)

def divide_nums(x,y) :
    # print('first # :', x)
    # print('second number :', y)
    z = int(x) / int(y)
    print('\ndivided: ',z)

def multiply_nums(x,y) :
    # print('first # :', x)
    # print('second number :', y)
    z = int(x) * int(y)
    print('\nmultiplied: ',z)